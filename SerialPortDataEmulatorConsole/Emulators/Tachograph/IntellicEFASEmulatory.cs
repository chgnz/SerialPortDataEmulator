        // interval settings
        private UInt32 TxInterval;
        private SerialPort Port;

        // private vars
        private long Timestamp;
        private uint VdoMsgIndex;

        public void Init(SerialPort port)
        {
            this.Port = port;
            this.Port.StopBits = StopBits.One;
            this.Port.BaudRate = GetBaudrate();
            this.TxInterval = GetTxInterval();

            this.Port.Open();

            Console.WriteLine($"Intellic EFAS D8 Emulator Initialized. {port.PortName}, Baudrate: {GetBaudrate()}, Tx interval: {GetTxInterval()} ms");

            Timestamp = new DateTimeOffset(DateTime.UtcNow).ToUnixTimeSeconds();
        }

        public void Trigger()
        {
            if (this.TxInterval == 0 ||
                GetTimestamp() - this.Timestamp < this.TxInterval)
            {
                // nav pienācies laiks sūtīt
                return;
            }

            this.Send();

            this.Timestamp = GetTimestamp();
        }

        bool Send()
        {
            if (!Port.IsOpen)
            {
                return false;
            }
            Console.WriteLine($"EFAS D8 Emulator Send {EFAS_MSG[VdoMsgIndex].Length} bytes: {BitConverter.ToString(EFAS_MSG[VdoMsgIndex]).Replace("-","")}");

            Port.Write(EFAS_MSG[VdoMsgIndex], 0, EFAS_MSG[VdoMsgIndex].Length);
            VdoMsgIndex++;
            VdoMsgIndex = (uint)(VdoMsgIndex % EFAS_MSG.Count());

            return true;
        }

        private long GetTimestamp()
        {
            return new DateTimeOffset(DateTime.UtcNow).ToUnixTimeMilliseconds();
        }

        private int GetBaudrate()
        {
            return 10400;
        }

        private UInt32 GetTxInterval()
        {
            return 1000;
        }

        public string GetMenuString()
        {
            return "Intellic EFAS (automatically transmits EFAS data each 1000ms @ baudrate 10400)";
        }
